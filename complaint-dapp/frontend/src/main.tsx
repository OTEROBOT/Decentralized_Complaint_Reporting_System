/** * ============================================================
 * ไฟล์: main.tsx
 * หน้าที่: เป็นจุดเริ่มต้นหลัก (Entry Point) ของแอปพลิเคชัน React ทั้งหมด
 * ไฟล์นี้จะทำหน้าที่เชื่อมต่อระหว่าง Code ของ React กับหน้าจอ HTML จริงๆ 
 * ============================================================
 */

// นำเข้า StrictMode จาก React เพื่อช่วยตรวจสอบข้อผิดพลาดในโปรแกรม
// StrictMode จะช่วยเตือนเมื่อเราเขียน Code ที่ล้าสมัย หรือมีความเสี่ยงจะเกิด Bug
// (หมายเหตุ: ในโหมด Development มันอาจจะทำให้บางฟังก์ชันทำงาน 2 รอบเพื่อเช็คความเสถียร)
import { StrictMode } from 'react'

// นำเข้า createRoot จาก react-dom/client ซึ่งเป็น API หลักใน React 18 ขึ้นไป
// ทำหน้าที่เป็นตัวสร้าง "จุดกำเนิด" ของ React DOM เพื่อนำไปใส่ในไฟล์ HTML
import { createRoot } from 'react-dom/client'

// นำเข้าไฟล์ CSS หลัก (Global Styles) เพื่อใช้งานการตกแต่งทั่วทั้งแอปพลิเคชัน
// การ import แบบนี้ทำให้ CSS ถูกรวมเข้าไปในขั้นตอน Build โดยอัตโนมัติ
import './index.css'

// นำเข้า Component หลักที่ชื่อว่า 'App' จากไฟล์ App.tsx
// App มักจะเป็นศูนย์กลางที่รวมเอา Component ย่อยๆ ทั้งหมดของระบบไว้ด้วยกัน
import App from './App.tsx'

/**
 * ขั้นตอนการ Render (การวาด UI ลงบนหน้าจอ):
 * 1. document.getElementById('root')! -> ไปหา Element ในไฟล์ index.html ที่มี id="root"
 * เครื่องหมาย '!' (Non-null assertion) บอก TypeScript ว่า "ไม่ต้องห่วง มี Element นี้อยู่จริงแน่นอน"
 * * 2. createRoot(...) -> สร้างรากเหง้าของ React ขึ้นมาที่ตำแหน่ง root นั้น
 * * 3. .render(...) -> สั่งให้ React เริ่มต้นวาดสิ่งที่อยู่ในวงเล็บลงไปที่หน้าจอ
 */
createRoot(document.getElementById('root')!).render(
  // ใช้ <StrictMode> ครอบ <App /> ไว้เพื่อเปิดใช้งานโหมดตรวจสอบความปลอดภัยตามที่กล่าวข้างต้น
  <StrictMode>
    
    {/* เรียกใช้งาน Component หลัก คือ App ซึ่งจะบรรจุเนื้อหาทั้งหมดของเว็บเรา */}
    <App />

  </StrictMode>,
)

/**
 * สรุปสั้นๆ: 
 * ไฟล์นี้เหมือน "ปลั๊กไฟ" ที่เอาตัวแอป (App.tsx) ไปเสียบเข้ากับ 
 * โครงสร้างของหน้าเว็บ (index.html) เพื่อให้แสดงผลออกมาได้นั่นเอง
 */